from typing import List, Any
import numpy as np

class BasicMath:
    def plus(self, a: float, b: float) -> float:
        return float(a) + float(b)
        
    def minus(self, a: float, b: float) -> float:
        return float(a) - float(b)
        
    def times(self, a: float, b: float) -> float:
        return float(a) * float(b)
        
    def divide(self, a: float, b: float) -> float:
        return float(a) / float(b) 